package Cliente;

public class Cliente {

    String nombre; 
    String apellidos; 
    int edad;
/**
 * 
 * @param nombre Nombre del cliente
 * @param apellidos	Apellido del cliente
 * @param edad	Edad del cliente
 */
    public Cliente(String nombre, String apellidos, int edad) {
        super();
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
    } 


}

